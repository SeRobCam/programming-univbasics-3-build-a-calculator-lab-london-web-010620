def addition(num1, num2)
  sum = num1 + num2
  return sum
end

def subtraction(num1, num2)
  subs = num1 - num2
  return subs
end

def division(num1, num2)
  divis = num1 / num2
  return divis
end

def multiplication(num1, num2)
  multip = num1 * num2
  return multip
end

def modulo(num1, num2)
  rem = num2 - (num2/num1)
  return rem
end

def square_root(num)
  square = Math.sqrt(num)
  return square
end